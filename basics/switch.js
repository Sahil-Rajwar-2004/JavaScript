let x = 2;
let y = 0;
let operator = "/";

switch(operator){
    case "+":
        console.log(x+y);
        break;
    case "-":
        console.log(x-y);
        break;
    case "*":
        console.log(x*y);
        break;
    case "/":
        console.log(x/y);
        break;
    default:
        console.log("Invalid operator");
}
